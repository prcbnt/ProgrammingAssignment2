## [Here is an explanation of what the code does :
# makeCacheMatrix function :
## get() returns original matrix
## setinverse() computes, caches, and returns the matrix inverse
## getinverse() returns matrix inverse
## cacheSolve() returns cached matrix inverse using previously calculated matrix inverse in maheCacheMatrix function]

makeCacheMatrix <- function(x = matrix()) {
  m <- NULL
  set <- function(y) {
    x <<- y
    m <<- NULL
  }
  get <- function() x
  setinverse <- function(solve) m <<- solve
  getinverse <- function() m
  list(set = set, get = get,
       setinverse = setinverse,
       getinverse = getinverse)
}


cacheSolve <- function(x, ...) {
  ## Return a matrix that is the inverse of 'x'
  m <- x$getinverse()
  if(!is.null(m)) {
    message("getting cached data")
    return(m)
  }
  data <- x$get()
  m <- solve(data, ...)
  x$setinverse(m)
  m
}
